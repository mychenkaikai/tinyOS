.macro ISR_NOERR vector
    .global interrupt_stub_\vector
interrupt_stub_\vector:
    push 0
    push \vector
    jmp interrupt_common
.endm

.macro ISR_ERR vector
    .global interrupt_stub_\vector
interrupt_stub_\vector:
    push \vector
    jmp interrupt_common
.endm

interrupt_common:
    cld
    PUSH_REGS
    mov rdi, rsp
    call x86_64_interrupt_dispatch
    POP_REGS
    add rsp, 16
    iretq

x86_64_interrupt_stub_default:
    push 0
    push 255
    jmp interrupt_common

